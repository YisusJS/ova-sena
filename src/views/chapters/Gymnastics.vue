          <strong>Rollo adelante</strong> <br />
          <strong>Descripción técnica del movimiento</strong> Se parte de la
          posición flexionada en punta de pies (cuclillas) manos apoyadas sobre
          el piso, brazos a la anchura de los hombros y perpendicular a ellos,
          realizar enseguida un empuje de piernas por parte de la punta de los
          pies, pegar la barbilla al pecho y arquear al máximo la espalda.
          Flexión completa de brazos permitiendo el contacto de la espalda con
          el suelo a través de una rodada hasta llegar nuevamente a la posición
          de cuclillas. La cabeza no debe tocar en ningún momento el suelo
          durante todo el transcurso del movimiento. Las piernas deben
          mantenerse cerradas y rodillas pegadas. A nivel de enseñanza escolar
          se le debe recomendar al niño tomarse las rodillas con las dos manos
          al final del movimiento. Se debe evitar que el niño ejecute la rodada
          al frente sin apoyar las manos. Aunque existen formas de auxiliar el
          rollo adelante, este se puede ejecutar sin ayuda. <br />Pre -
          requisitos: Fuerza de brazos Buen balanceo de espalda Pegar la
          barbilla al pecho <br /><br />
          <strong>Metodología</strong> <br />Balanceo completo de adelante hacia
          atrás en posición agrupada tomando las rodillas con las manos y
          barbilla pagada al pecho <br />Balanceo del cuerpo adelante y atrás en
          posición agrupada llegar a la posición cuclillas y regresar a la
          posición inicial. <br />El mismo balanceo anterior, pero al final de
          una colchoneta para terminar la parte final del balanceo en posición
          cuclillas sobre el piso a un nivel más bajo. <br />Balanceo en
          posición agrupada finalizar en posición cuclillas seguidamente
          realizar salto de extensión. Realizar balanceo de espaldas en posición
          agrupada y colocarse de pie sin ayuda alguna <br />Tomar posición de
          rodillas sobre una banca sueca, apoyar las manos, rodar adelante hasta
          conseguir la posición cuclillas. <br />
          La cabeza debe estar por debajo de la banca sueca. <br />Realizar el
          rollo adelante sobre un plano inclinado y repetir varias veces.
