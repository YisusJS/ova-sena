          <strong>Descripción técnica del movimiento</strong> Desde la posición
          de cuclillas dejarse caer hacia atrás sobre glúteos, espalda y nuca.
          Rápidamente las manos, se trasladarán hacia atrás y se apoyarán sobre
          el piso a la altura de la cabeza con la punta de los dedos mirando
          hacia adelante al mismo tiempo la parte posterior de la cabeza tocará
          el suelo y la barbilla el pecho los brazos se apoyarán retirados del
          cuerpo a la anchura de los hombros, luego empujarán fuerte para llevar
          el cuerpo nuevamente hacia la posición de cuclillas.
