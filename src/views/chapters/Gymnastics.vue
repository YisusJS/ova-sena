      </div>
      <div class="module__content">
        <p>
          Las variedades de la gimnasia deportiva son: la gimnasia artística, la
          gimnasia rítmica deportiva y la acrobacia. Estas variedades son
          típicamente de orientación deportiva; sus objetivos son dominar los
          ejercicios gimnásticos de gran dificultad y conseguir resultados
          deportivos satisfactorios. <br />
          Estas variedades necesitan una gran preparación física, técnica,
          táctica y volitiva para conseguir sus objetivos. La gimnasia artística
          la practican tanto los varones como las hembras, aunque varían los
          contenidos y también los eventos en que se compite. La gimnasia
          artística masculina tiene una programación de seis eventos: ejercicios
          a manos libres, caballo con arzones, anillas, caballo de salto, barras
          paralelas y barra fija, mientras que la femenina tiene cuatro: caballo
          de salto, paralelas asimétricas, viga de equilibrio y ejercicios a
          manos libres. Como puede observarse, hay dos eventos comunes para
          hembras y varones, pero la posición y altura del aparato en un caso
          (salto) Y el contenido de sus ejercicios en otro (ejercicios a manos
          libres) los diferencian. En los ejercicios a manos libres masculinos
          predominan los ejercicios acrobáticos, además de algunos ejercicios de
          fuerza y equilibrio, mientras que en el femenino su contenido posee
          una gran cantidad de elementos de baile y coreográficos, aunque
          también se realizan ejercicios acrobáticos, todo ello con
          acompañamiento musical, elemento definitorio en la diferencia con el
          masculino. Esta variedad desarrolla la fuerza rápida, rapidez,
          resistencia, agilidad y flexibilidad, así como las cualidades
          volitivas, ya que para su práctica se necesita vencer dificultades
          presentadas por los propios ejercicios, como el riesgo, a la vez que
          se necesita realizar estos ejercicios con virtuosismo. La gimnasia
          rítmica deportiva es una variedad practicada sólo por mujeres, que
          desarrolla la coordinación, el ritmo y el sentido musical a través de
          la ejecución de los ejercicios a manos libres y con implementos (aros,
          clavas, cintas, pelotas y cuerdas). Los ejercicios van acompañados de
          la música, lo cual es una particularidad metodológica esencial en la
          enseñanza de este bello deporte. Otra importancia vital en su práctica
          es el conseguir el dominio de los distintos implementos, pues el
          grueso de las competencias depende de la ejecución de ejercicios con
          ellos ya que, tanto en la competencia individual como en la colectiva
          el buen desempeño de las atletas depende de este factor. Podemos decir
          que la gimnasia rítmica deportiva es una expresión artístico -
          deportiva, sobre la base de la unión de los ejercicios gimnásticos sin
          y con implementos y la interpretación musical. Por último, tenemos la
          acrobacia como otra variedad deportiva de la gimnasia. Esta variedad
          es practicada igualmente por varones y hembras, ya sean niños, jóvenes
          o adultos. Desarrolla las cualidades físicas y volitivas de las
          personas y sus ejercicios se usan en la preparación de un gran número
          de deportistas. Con estos ejercicios se desarrollan la fuerza, la
          flexibilidad, la rapidez de reacción y la agilidad. Se perfecciona la
          coordinación de movimientos y se educa la audacia y la voluntad. Los
          movimientos de giros, que en ella se realizan, desarrollan la
          orientación en el espacio y por tanto desarrollan el aparato
          vestibular. Los ejercicios acrobáticos constan de elementos estáticos
          y de giros, y están divididos en tres grupos: <br />
          1. Los saltos acrobáticos, que son ejercicios con giros completos o
          parciales (volteos, volteretas y vueltas).<br />
          2. Balanceos, que consisten en ejercicios de equilibrio y ponderación
          (individuales, en parejas, grupos, invertidos y toda clase de apoyos.
          <br />
          3. Ejercicios de rebote, unidos a lanzamientos y capturas del
          ejecutante; estos son realizados con giros de todo tipo. Los eventos
          que la integran son los siguientes: <br />- Cama elástica. <br />-
          Estera de acrobacia. <br />- Ejercicios de dúos, trías y cuartetos
          (masculinos, femeninos y mixtos). La acrobacia está considerada dentro
          de la gimnasia deportiva como la actividad de mayor interacción
          neuromuscular del movimiento, entre el espacio, el tiempo y la
          intensidad.
