          <strong>Educativa y de desarrollo </strong> <br />
          Este grupo comprende un sistema de ejercicios especialmente
          gimnásticos, que crean los hábitos vitales para la vida, conservan la
          salud y desarrollan las capacidades motrices. Dentro de este grupo
          juegan un papel decisivo los diversos sistemas, métodos y
          procedimientos de organización que encauzan con eficacia la práctica
          de los ejercimos gimnásticos. Existen a su vez dos tipos fundamentales
          de gimnasia que poseen orientación educativa y de desarrollo, que son
          las llamadas básicas y las aplicadas. Los tipos de gimnasia básica
          tienen como objetivos fundamentales fortalecer la salud, contribuir al
          desarrollo normal de todos los órganos y sistemas del organismo,
          formar funciones motoras y desarrollar capacidades motrices. Los
          ejercicios que se utilizan en estos tipos de gimnasia deben ser
          accesibles y bien seleccionados para que ejerzan la influencia
          deseada, se deben realizar con diferentes partes del cuerpo, con
          distinta velocidad y amplitud, en distintas direcciones y con
          esfuerzos musculares diferentes.
          <br /><br />
          <strong>Higiénica</strong> <br />Este grupo de la gimnasia está
          orientado al mantenimiento de la salud también a su recuperación
          cuando se ha perdido, ya sea por un trauma, una enfermedad u otras
          razones. El grupo de las gimnasias higiénicas tiene dos tipos
          fundamentales de manifestación: la higiénica propiamente dicha y la
          terapéutica, los que a su vez poseen variedades que explicaremos
          seguidamente: Variedades higiénicas La gimnasia de la producción es
          una variedad del tipo de las llamadas gimnasias higiénicas. Está
          insertada en el régimen diario del trabajo, por lo que su objetivo
          principal es contribuir a elevar el rendimiento en el trabajo. Existen
          dos formas de aplicación de la gimnasia de la producción:
          <br /><br />1. Gimnasia de introducción, que acelera la entrada al
          trabajo y la sincronización funcional indispensable para el mismo.
          <br />2. La pausa de cultura física que ayuda a eliminar el
          agotamiento y favorece una fluidez más activa de los procesos
          restablecedores. Esta se realiza en momentos en que la productividad
          del trabajador baja por motivos de cansancio. Esta gimnasia se realiza
          diariamente en el taller, laboratorio, fábrica, etc. antes de comenzar
          el trabajo o en recreos especiales durante el mismo. Al realizar esta
          gimnasia se resuelven los problemas siguientes: Contribuir a
          incorporar a la persona a la actividad laboral. Asegurar el descanso
          durante la jornada laboral para elevar la capacidad de trabajo
          mediante el descanso activo. Mejorar la salud y el desarrollo físico
          de los trabajadores. Contribuir a educar el colectivismo, organización
          y aumento de la disciplina. Incorporar a los trabajadores a la
          práctica sistemática de los ejercicios físicos. Podemos decir que la
          gimnasia de la producción es la aplicación de los ejercicios físicos,
          como forma de descanso activo a los trabajadores con el fin de
          incrementar la eficiencia productiva y elevar el nivel de salud de los
          mismos. Otra variedad higiénica es la llamada gimnasia musical
          aerobia, de gran aceptación por parte de las mujeres, que son las que
          más la practican. Entre sus objetivos se encuentran el de educar una
          buena figura, fortalecer la salud y desarrollar armónicamente los
          músculos de todo el cuerpo, y sobre toda la fuerza, la flexibilidad y
          en especial la resistencia aerobia, además de ampliar el sentido
          musical y el ritmo. Esta gimnasia se practica preferentemente en
          grupos y con acompañamiento musical. Su práctica es diaria y la carga
          se aplica progresivamente, elevando la cantidad de repeticiones, así
          como el tiempo de realización. La gimnasia atlética forma parte
          también del grupo de las higiénicas; está orientada a desarrollar
          armónicamente los músculos de todo el cuerpo, formar una figura
          armónica y desarrollar además la fuerza y la agilidad. Los ejercicios
          físicos que se recomiendan para practicar la gimnasia atlética se
          subdividen en tres grados de dificultad: ligeros, de dificultad media
          y difíciles. Se debe tener en cuenta esto y empezar con los de menor
          dificultad a los de mayor dificultad. Durante la práctica de la
          gimnasia atlética se deben observar las reglas siguientes: Controlar
          la salud, o sea, una o dos veces al año hacerse un examen médico.
          Controlar el desarrollo físico y algunos de sus índices fundamentales
          de desarrollo: capacidad vital, presión sanguínea y peso, fuerza de
          las manos, tronco y piernas, circunferencia del pecho, hombros, cadera
          y pantorrilla. Observar las reglas higiénicas de los practicantes y
          del local de práctica. Controlar la figura (silueta). Aprender a
          elaborar tablas de ejercicios, teniendo en cuenta sus propias
          particularidades. Leer sobre esta variedad de la gimnasia, sobre sus
          métodos de desarrollo de la fuerza, agilidad, etcétera. Variedades
          terapéuticas Entre las variedades terapéuticas se encuentra la llamada
          gimnasia correctiva, encaminada a corregir defectos congénitos o
          adquiridos durante la vida, mediante ejercicios especiales que deben
          ser realizados después de la consulta con un médico. Esta gimnasia se
          practica en grupos ocasionalmente, pero la mayoría de las veces se
          realiza en forma individual atendiendo a la deformación que presenta
          cada individuo. Debe controlarse sistemáticamente la influencia de los
          ejercicios en los practicantes. La gimnasia de rehabilitación es otra
          variedad terapéutica orientada a restablecer la salud y la capacidad
          de trabajo, por lo cual los ejercicios a realizar dependen de la
          enfermedad concrete y por tanta san recetados siempre por un médico,
          teniendo también en cuenta la preparación física, la edad y el seso de
          los participantes. Su práctica puede ser individual y en grupos. En
          sus clases, los metodólogos observan que la respiración sea correcta,
          comprueban sistemáticamente el pulso, controlan el esfuerzo alternado
          con el descanso y en general, la influencia de los ejercicios en los
          enfermos. Podemos definir esta gimnasia como la prevención y el
          restablecimiento morfo - funcional del organismo, utilizando los
          medios y métodos de la cultura física. La llamada gimnasia funciona/
          es otra de las variedades del tipo de las terapéuticas, encaminada
          esencialmente a mantener y mejorar las funciones vitales del
          organismo, como son los procesos respiratorio, circulatorio,
          cardiovascular, digestivo, etcétera. Este tipo de gimnasia se realiza
          bajo orientación médica y es impartida por especialistas, cuyos
          conocimientos abarcan tanto la rama de la medicina terapéutica como la
          de la cultura física. Se utilizan para su impartición ejercicios
          sencillos de desarrolla físico general, teniendo. en cuenta las reglas
          siguientes: 1. Los ejercicios deben corresponder a la edad y
          preparación física del participante. 2. Los ejercicios deben tener una
          influencia multilateral, realizándose con distintas partes del cuerpo,
          en distintas direcciones, cambiando la velocidad y el carácter de los
          esfuerzos musculares. 3. Deben incluirse ejercicios posturales y para
          distintos tipos de respiración (pectoral, diafragmática, etcétera).
          Los ejercicios deben repetirse entre cuatro y ocho veces y cambiarse
          cada dos o tres semanas, aumentando la carga poco a poco. Esta
          gimnasia debe realizarse diariamente, entrando en el régimen de vida
          del individuo y debe llegara ser una necesidad vital del mismo. <br />
          <br />
          <strong>Gimnasia deportiva</strong> <br />Las variedades de la
          gimnasia deportiva son: la gimnasia artística, la gimnasia rítmica
          deportiva y la acrobacia. Estas variedades son típicamente de
          orientación deportiva; sus objetivos son dominar los ejercicios
          gimnásticos de gran dificultad y conseguir resultados deportivos
          satisfactorios. Estas variedades necesitan una gran preparación
          física, técnica, táctica y volitiva para conseguir sus objetivos. La
          gimnasia artística la practican tanto los varones como las hembras,
          aunque varían los contenidos y también los eventos en que se compite.
          La gimnasia artística masculina tiene una programación de seis
          eventos: ejercicios a manos libres, caballo con arzones, anillas,
          caballo de salto, barras paralelas y barra fija, mientras que la
          femenina tiene cuatro: caballo de salto, paralelas asimétricas, viga
          de equilibrio y ejercicios a manos libres. Como puede observarse, hay
          dos eventos comunes para hembras y varones, pero la posición y altura
          del aparato en un caso (salto) Y el contenido de sus ejercicios en
          otro (ejercicios a manos libres) los diferencian. En los ejercicios a
          manos libres masculinos predominan los ejercicios acrobáticos, además
          de algunos ejercicios de fuerza y equilibrio, mientras que en el
          femenino su contenido posee una gran cantidad de elementos de baile y
          coreográficos, aunque también se realizan ejercicios acrobáticos, todo
          ello con acompañamiento musical, elemento definitorio en la diferencia
          con el masculino. Esta variedad desarrolla la fuerza rápida, rapidez,
          resistencia, agilidad y flexibilidad, así como las cualidades
          volitivas, ya que para su práctica se necesita vencer dificultades
          presentadas por los propios ejercicios, como el riesgo, a la vez que
          se necesita realizar estos ejercicios con virtuosismo. La gimnasia
          rítmica deportiva es una variedad practicada sólo por mujeres, que
          desarrolla la coordinación, el ritmo y el sentido musical a través de
          la ejecución de los ejercicios a manos libres y con implementos (aros,
          clavas, cintas, pelotas y cuerdas). Los ejercicios van acompañados de
          la música, lo cual es una particularidad metodológica esencial en la
          enseñanza de este bello deporte. Otra importancia vital en su práctica
          es el conseguir el dominio de los distintos implementos, pues el
          grueso de las competencias depende de la ejecución de ejercicios con
          ellos ya que, tanto en la competencia individual como en la colectiva
          el buen desempeño de las atletas depende de este factor. Podemos decir
          que la gimnasia rítmica deportiva es una expresión artístico -
          deportiva, sobre la base de la unión de los ejercicios gimnásticos sin
          y con implementos y la interpretación musical. Por último, tenemos la
          acrobacia como otra variedad deportiva de la gimnasia. Esta variedad
          es practicada igualmente por varones y hembras, ya sean niños, jóvenes
          o adultos. Desarrolla las cualidades físicas y volitivas de las
          personas y sus ejercicios se usan en la preparación de un gran número
          de deportistas. Con estos ejercicios se desarrollan la fuerza, la
          flexibilidad, la rapidez de reacción y la agilidad. Se perfecciona la
          coordinación de movimientos y se educa la audacia y la voluntad. Los
          movimientos de giros, que en ella se realizan, desarrollan la
          orientación en el espacio y por tanto desarrollan el aparato
          vestibular. Los ejercicios acrobáticos constan de elementos estáticos
          y de giros, y están divididos en tres grupos: <br />
          <br />1. Los saltos acrobáticos, que son ejercicios con giros
          completos o parciales (volteos, volteretas y vueltas). <br /><br />2.
          Balanceos, que consisten en ejercicios de equilibrio y ponderación
          (individuales, en parejas, grupos, invertidos y toda clase de apoyos.
          <br /><br />3. Ejercicios de rebote, unidos a lanzamientos y capturas
          del ejecutante; estos son realizados con giros de todo tipo. Los
          eventos que la integran son los siguientes: - Cama elástica. - Estera
          de acrobacia. - Ejercicios de dúos, trías y cuartetos (masculinos,
          femeninos y mixtos). La acrobacia está considerada dentro de la
          gimnasia deportiva como la actividad de mayor interacción
          neuromuscular del movimiento, entre el espacio, el tiempo y la
          intensidad.
