          <strong>Reglamento de la marcha atlética</strong><br />
          Las competencias de marcha se podrán realizar en la pista atlética
          estándar, por lo general en distancias hasta 10.000 m. Las pruebas de
          distancias superiores se realizan en un circuito que no será superior
          a 2.5 km, ni inferior a 2 km en el caso de competiciones mundiales o
          de competencias del circuito olímpico. Para el resto de las
          competiciones el circuito no deberá ser superior a 2.5 km ni inferior
          a 1 km. En las pruebas que comiencen y finalicen en el estadio, el
          circuito estará situado tan cerca de éste como sea posible.
          <br /><br />Los atletas deben usar ropa que esté limpia, diseñada y
          llevada de forma que no sea reprobable. La ropa debe estar
          confeccionada con un tejido que no sea transparente ni aun estando
          mojado. Los atletas no podrán usar ropa que pueda impedir la visión de
          los Jueces. En la camiseta los atletas deben llevar el mismo color
          delante y detrás. Se recomienda el uso de ropa confeccionada con un
          tejido adecuado para lograr una alta comodidad de los atletas evitando
          quemaduras por fricción. <br /><br />
          <strong>Reglas básicas</strong><br />Reglamentariamente los marchistas
          no pueden perder visiblemente (a simple vista) su contacto con el
          suelo y tampoco podrán flexionar la rodilla en el momento de tomar
          contacto con el suelo. <br />
          <br /><strong>Avisos:</strong> Los atletas tienen que ser avisados
          cuando, por su modo de progresión, corren el riesgo de incumplir con
          estas premisas. Un disco amarillo con el símbolo en ambas caras de la
          irregularidad cometida les será mostrado. <br /><br /><strong
            >Tarjetas rojas</strong
          >: Cuando un Juez observa alguna infracción en cualquier momento de la
          prueba el Juez enviará una Tarjeta Roja al Juez Jefe. Cuando tres
          tarjetas rojas de tres jueces diferentes hayan sido enviadas al Juez
          Jefe del mismo atleta, el atleta será descalificado e informado de
          esta descalificación por el Juez Jefe o su adjunto mostrando al atleta
          un disco rojo. <br /><br /><strong
            >Puestos de esponjas, bebidas y avituallamiento</strong
          >: En la salida y llegada de todas las carreras habrá agua y otros
          refrescos apropiados. Para todas las pruebas hasta 10 km. inclusive,
          se pondrán puestos de esponjas/bebidas donde únicamente agua será
          proporcionada, con intervalos apropiados si las condiciones
          climatológicas aconsejan dicha provisión. <br /><br />Para todas las
          pruebas superiores a 10 km. se pondrán puestos de avituallamiento en
          cada vuelta. Además, se instalarán puestos de esponjas y bebidas,
          donde se facilitará únicamente agua, aproximadamente a mitad de camino
          entre los puestos de avituallamiento, o con más frecuencia, si las
          condiciones climatológicas aconsejan dicha provisión.
