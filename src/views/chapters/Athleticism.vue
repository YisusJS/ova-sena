      <div class="module__content">
        <p>
          <a class="cite">Makarov A. (s.f.)</a>. El
          <strong>Salto largo</strong> es un ejercicio físico natural y simple,
          que goza de gran significado práctico. El papel principal en salto
          largo lo desempeñan la carrera de impulso estable y rápida, la
          habilidad de pisar exactamente la tabla e impulsarse fuertemente,
          conservar el equilibrio en el vuelo y aterrizar racionalmente. A
          diferencia del salto alto, los movimientos del deportista en la fase
          de vuelo en los saltos de longitud gozan de significado secundario
          puesto que no influyen en la distancia de vuelo, sino ayudando
          solamente a mantener el equilibrio y prepararse para el aprendizaje.
          <strong>El resultado en salto largo</strong> depende, en última
          instancia, de aquella velocidad horizontal que se ha logrado en la
          carrera de impulso, y la velocidad vertical adquirida en el momento de
          tomar impulso. <br /><br />El salto largo ha existido en todas las
          competencias deportivas desde la antigüedad. Los griegos se encargaron
          de incluir esta modalidad dentro de los Juegos Olímpicos el año 776
          a.C., momento en el cual se realizaba el salto con halteras, que eran
          una especie de pesas, y las llevaban en cada mano. Atenas fue el
          primer lugar en donde se realizaron competencias incluyendo el salto
          de longitud. Empezó a ser parte de los juegos olímpicos a inicios de
          1896 y 1928 se les autorizo a las mujeres concurrir en esta
          disciplina. A principios del siglo XIX, Adam William logró ejecutar un
          salto de 5.41 metros de distancia. Otro importante representante fue
          Jhon Lane, quien logró saltar 7.05 metros en el año 1874. <br /><br />
          <strong>Técnica del salto largo</strong> <br />
          Figura 41 - <i>Ejercicios para extensión de la pierna adelantada</i>
          <br />
          <img :src="require('@/assets/modules/athleticism/e27.jpeg')" alt="" />
          <br />
          <a class="pie"> Técnica del salto largo. Makarov, A. (s.f.) </a>
          <br /><br />
          Para el estudio y enseñanza del salto largo se ha distribuido en las
          siguientes fases: <br /><br /><strong
            >• Carrera de aproximación</strong
          >, el atleta debe elaborar la más alta cuantía de energía aceptable,
          por una corrida anterior al salto. Toda esta energía que se va
          almacenando para convertir luego en un salto. Con la carrera se debe
          lograr el perfecto acercamiento del atleta a la tabla de batida,
          utilizando la distancia del pasillo, según sus capacidades específicas
          respecto a las condiciones de su carrera, con ello, se logra el
          correcto punto de batida. La velocidad adquirida durante el movimiento
          lineal brindara condiciones favorables para la siguiente fase la
          batida, de esta forma, se logrará la velocidad deseada en cada momento
          del desarrollo de la carrera, y la óptima al final, la colocación
          global y segmentaria correcta sobre la tabla de batida, para poder
          utilizar sus capacidades al más alto nivel de rendimiento, según la
          técnica que se utilice y función de las condiciones cinemáticas
          adquiridas previamente. <br />
          <br />
          La fase de la carrera posee tres (3) etapas: acción inicial,
          aceleración, zancada previa al impulso. Durante la primera etapa las
          zancadas son cortas y relajadas, durante la segunda etapa el
          participante sigue mejorando la amplitud de la zancada llegando al
          límite de la capacidad de aceleración del corredor, los pasos deben ir
          acompañados de un potente braceo. La etapa 3 de zancadas previas al
          impulso. se trata de los dos últimos pasos del salto de las cuales la
          penúltima será ligeramente más larga que los demás y la última, el más
          reducido. <br /><br />• La batida. Esta fase inicia cuando el pie se
          encuentra encima de la tabla, es la más importante y requiere muy
          buena coordinación para lograr optimo aprovechamiento de la primera
          fase. Para su estudio se dividirá en tres etapas: amortiguamiento,
          apoyo e impulso. El amortiguamiento o colocación del pie de batida en
          la tabla, esta posición se realizará con el metatarso y únicamente
          sobre la parte exterior del pie (arco de amortiguamiento), con la
          rodilla semi estirada, la pierna produce con la superficie un ángulo
          que modifica dentro de ciento quince y ciento veinte grados. En ese
          momento el tronco queda atrasado relativamente a la continuación del
          centro de longitud de la pierna de batida. Se levanta la cabeza con la
          mirada al frente. La pierna libre y los brazos ayudan a la
          coordinación del movimiento. <br /><br />• Etapa de apoyo el atleta
          puede estar en forma vertical del pie de la batida, y como resultado
          la pierna de batida se dobla en la cadera. El Impulso comienza cuando
          la vertical del apoyo sobre pasa el centro de gravedad del atleta
          extendiéndose fuertemente en las articulaciones de rodilla, cadera y
          tobillo, el muslo separado en ligero movimiento va hacia frente y la
          rodilla flexionada bien alto llega a la altura de la cadera. Como en
          la carrera los brazos van flexionados y un poco separados del tronco,
          de una forma sincronizada equilibra la actividad obstruyendo en el
          momento en que la mano adelantada alcanza la altura del rostro, cuando
          se eleva los hombres ahí se produce el bloqueo. El tronco debe
          mantenerse en vertical para favorecer el equilibrio del vuelo.
          Finaliza la batida cuando el dedo grande del pie sale propulsando. Los
          atletas emplean ángulos mayores cuando destacan la fuerza, los últimos
          pasos son más considerables en los cambios de ritmo y amplitud. Los
          más rápidos poseen los ángulos y trayectorias de vuelo más tensas.
          <br /><br />• El Impulso Comienza cuando la vertical del apoyo sobre
          pasa el centro de gravedad del atleta, extendiéndose fuertemente en
          las articulaciones de rodilla, cadera y tobillo. El muslo separado en
          ligero movimiento va hacia frente y la rodilla flexionada bien alto
          llega a la altura de la cadera. Como en la carrera los brazos van
          flexionados y un poco separados del tronco el cual debe mantenerse en
          vertical para favorecer el equilibrio del vuelo. La fase de batida
          finaliza cuando el primer dedo del pie científicamente llamado Hallux
          se despega del piso. Los atletas emplean ángulos mayores cuando poseen
          más fuerza, los últimos pasos aumentan en ritmo y amplitud.
          <br /><br />• Fase de vuelo, según
          <a class="cite">Makarov, A. (s.f.)</a>. El vuelo comienza con ángulo
          de 18 a 24 grados. El saltador efectúa durante el vuelo diferentes
          movimientos, dependiendo del estilo, con el fin de mantener el
          equilibrio y aterrizar de manera más conveniente. La primera parte del
          vuelo (despegue) es igual en todos los estilos de salto largo. La
          pierna de ataque se reflexiona en el vuelo con el fémur altamente
          elevado, va hacia delante, la pierna de impulso se queda atrás y el
          saltador adopta la posición del "paso amplio". Los movimientos
          posteriores dependen del estilo de salto adoptado por el atleta.
          <br />
          <br />
          El estilo <strong>"agrupado" o "natural"</strong> es el más sencillo.
          Con este precisamente comienza la enseñanza de los saltos de longitud
          en los niños. Después de vuelo "en el paso" la pierna de ataque se
          baja algo, la pierna de impulso se lleva hacia ella, y ambas piernas
          se acercan al pecho. De esta manera, el saltador adopta la posición
          agrupada con los brazos abajo. Para el momento del aterrizaje el
          deportista extiende las piernas tratando de lanzarlas lo más lejos
          posible hacia delante, llevando simultáneamente los brazos hacia
          atrás. La deficiencia de este estilo consiste en la posible rotación
          hacia delante en el vuelo, lo que hace disminuir sustancialmente la
          distancia de salto. <br />
          <br />
          El estilo <strong>"en suspensión</strong>" es más eficaz, ya que
          permite evitar la rotación y facilitar el aterrizaje. Después del
          despegue la pierna de ataque se baja y se lleva hacia atrás junto con
          la de impulso. La pelvis se adelanta, los brazos efectúan un
          movimiento arqueado hacia adelante-abajo-atrás y por los lados hacia
          arriba. El deportista extiende el tronco y lleva los hombros un poco
          hacia atrás. Luego toma la posición inicial antes del aterrizaje.
          <br /><br />
