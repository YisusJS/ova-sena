          <br />
          <img :src="require('@/assets/modules/athleticism/e28.jpeg')" alt="" />
          <br />
          <a class="pie"> Técnica del salto largo. Makarov, A. (s.f.) </a>
          <br /><br />El estilo de
          <strong>"tijeras" o “correr en el aire”</strong> se diferencia algo
          del anterior, puesto que el saltador no adopta alguna posición fija en
          el vuelo, sino que efectúa los movimientos casi iguales a los de la
          carrera de impulso. En cierto sentido, sigue "corriendo por el aire".
          Después del despegue la pierna de ataque se baja y se lleva hacia
          atrás y la de impulso se lleva hacia delante (primer paso). El
          descenso de la pierna de ataque se acompaña con la puesta de la pelvis
          hacia delante. Luego, es la pierna de impulso la que se lleva hacia
          atrás y la de ataque hacia delante. En este proceso las piernas se
          quedan flexionadas en las articulaciones de la rodilla, los brazos se
          mueven al ritmo de los movimientos de las piernas. Al prepararse para
          el aterrizaje, el saltador une las piernas, las lleva hacia el pecho y
          luego las lanza hacia delante. De este modo, el estilo de tijera
          presupone que el saltador, efectuando casi los mismos movimientos que
          en la carrera, hace 2,5 o 3,5 pasos en el vuelo (lo que depende de la
          longitud del salto). <br /><br />• El
          <strong>aterrizaje</strong> puede influir considerablemente en el
          resultado del salto, por lo tanto, hace falta dedicar mucha atención a
          éste desde las etapas iniciales de la enseñanza. El aterrizaje se
          efectúa de manera igual en todos los estilos de salto largo. Las
          piernas flexionadas suben de tal manera, que los talones estén un poco
          por debajo del nivel de la pelvis, y se lanzan hacia delante. El
          aterrizaje culmina con la flexión de las piernas y la salida hacia
          delante o la caída al lado. En el momento de lanzar las piernas hacia
          delante el saltador puede encontrarse en la posición agrupado con
          inclinación considerable del tronco hacia delante o en la posición de
          sentado, lo que será más racional. <br />
