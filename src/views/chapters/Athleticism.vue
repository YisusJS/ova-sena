        <h2 class="module__title">Técnica de la carrera en fondo</h2>
      </div>
      <div class="module__content">
        <p>
          Las técnicas utilizadas en la carrera son la circular y la pendular,
          denominación que reciben según la figura que describen las rodillas y
          tobillos en el momento de correr, la empleada en las pruebas de
          velocidad, medio fondo y fondo es la circular cuyos movimientos
          facilitan la ejecución de la fase de vuelo y amplitud de zancada a
          diferencia de la pendular propia de la marcha atlética teniendo en
          cuenta que no se debe observar fase de vuelo. <br /><br />
          Son características morfológicas, funcionales y psicológicas de los
          atletas de las pruebas de fondo las extremidades largas y delgadas,
          talla mediana niveles altos de máximo consumo de oxígeno, técnicamente
          dotados de gran frecuencia y amplitud de zancada y con gran capacidad
          mental de soportar esfuerzos largos. <br /><br />
          Los corredores de medio fondo se distinguen por la frecuencia de
          apoyos y su amplitud de zancada: el corredor de medio fondo adopta una
          zancada de mayor amplitud con una frecuencia de apoyo elevada,
          mientras que el corredor de fondo reduce considerablemente esta
          amplitud, pero con una frecuencia mayor. Sin embargo, en la
          observación de atletas maratonistas de la élite mundial se evidencia
          buena amplitud de zancada. <br /><br />
          El rendimiento de un atleta depende de la eficiencia mecánica de la
          carrera y de la optimización de la capacidad aeróbica o anaeróbica
          según la distancia, sin olvidar otras variables que inciden en el
          logro de altos logros como son la capacidad mental de soportar
          esfuerzos prolongados, la recuperación adecuada y la alimentación
          entre otros. <br /><br />
          Cada persona posee una forma de correr la cual es caracterizada por su
          composición corporal, la longitud de las extremidades , el movimiento
          de las articulaciones , la fuerza y las diferentes tensiones
          musculares , sin embargo comprendiendo y entendiendo estas diferencias
          individuales, existen unas pautas generales de eficiencia motriz y
          biomecánica que indican como debe ser la técnica de la carrera,
          zancada de un atleta y la descomposición de la misma, en distintas
          partes o fases, para poder comprender y lograr mayor eficiencia
          técnica. <br /><br />
          La carrera para el estudio y análisis de la técnica de un atleta se
          puede descomponer en las siguientes fases: impulsión, vuelo,
          amortiguamiento, sostén, acción de tronco y brazos. En párrafos
          anteriores se había expuesto que no se encuentran atletas que puedan
          correr igual debido a características morfológicas, físicas y
          funcionales, pero en este párrafo se hace énfasis a otra
          característica que marca gran diferencia entre dos sujetos y es la
          referente a la forma de ejecución de cada una de las fases de la
          carrera, pero sin lugar a duda las distintas carreras desde el Sprint
          hasta el maratón tienen en común ciertos principios mecánicos. Rius
          <a class="cite" target="_blank"
            >(cómo se citó en Tobar y Zurata, 2013)</a
          >
          define las fases. <br /><br />
          <strong>Impulsión</strong>
          <br />
          Inicia en el momento de despegue del pie con respecto al suelo, en las
          carreras rápidas la impulsión se inicia desde el metatarso, y en la
          carrera lenta va de atrás del metatarso a la punta del pie. En esta
          fase es de gran importancia realizar el impulso desde el metatarso
          sintiendo el apoyo del pie y colocando atención a la dirección de la
          punta hacia el frente de este. El impulso realizado desde el metatarso
          disminuye la superficie de rozamiento del pie con el piso
          contribuyendo con el mejoramiento de reacción de los grupos musculares
          involucrados. <br /><br />
          El impulso inicia con la pierna completamente extendida, aunque esta
          extensión puede tener una mayor o menor duración dependiendo de las
          características del atleta y de la distancia a recorrer, por ejemplo
          para las carreras de velocidad la duración de la extensión de la
          pierna es mayor teniendo en cuenta que en este tipo de pruebas la
          amplitud de la zancada es mayor, esta fase se produce por la extensión
          del pie y la rodilla, iniciándose cuando las caderas, que vienen
          proyectadas por el impulso precedente, se encuentran en el punto más
          bajo de su trayectoria y han pasado a estar perpendiculares a la base
          metatarsiana del dedo pulgar para terminar algo más avanzadas con
          objeto de facilitar el impulso y no hacer una parábola excesivamente
          alta, aprovechando más la proyección del cuerpo hacia adelante. Otros
          autores afirman que la fase de impulso comienza cuando el centro de
          gravedad sobrepasa el punto de apoyo. Una vez que el centro de
          gravedad sobrepasa la perpendicular trazada desde su punto de apoyo,
          se produce una extensión por parte de las articulaciones, (cadera,
          rodilla, tobillo) finalizando al abandonar la punta del pie el suelo.
          Esta acción desplaza la masa del corredor adelante y arriba.
          <br /><br />
          <strong>La fase aérea o de vuelo</strong>
          <br />
          En esta fase la altura de la rodilla describe una parábola la cual
          debe contribuir con la amplitud de la zancada. La fase aérea comienza
          cuando el centro de gravedad pasa delante de la cabeza de los
          metatarsianos de la pierna de impulso y termina cuando el pie de la
          pierna libre toma contacto con el suelo. Esta fase es la transición
          entre el impulso y el apoyo. Se caracteriza por la acción circular del
          talón que ha finalizado el impulso. La pierna se flexiona en la
          rodilla y el talón va hacia el glúteo del cual no se despega hasta
          bien entrada la fase de impulsión. La cadera avanza, proyectadas hacia
          delante de acuerdo con la parábola resultante de la fuerza aplicada.
          Es aquí cuando el pie abandona el suelo elevándose por detrás, al
          mismo tiempo que se flexiona la pierna sobre el muslo de acción
          automática como reflejo de impulso. (p.p. 19-20)
          <br />
          <br />
          <strong>Amortiguamiento</strong>
          <br />
          Algunos autores consideran que el amortiguamiento inicia cuando el pie
          de la pierna que se apoya recibe el peso del cuerpo, la cual debe
          llegar a este contacto ligeramente flexionada y con la parte exterior
          del metatarso. <br /><br />
          Diez García
          <a class="cite">(cómo se citó en Tobar y Zurata, 2013)</a>, plantea
          que el pie toma contacto con el suelo nuevamente con el metatarso,
          generalmente por su parte externa (realizando una ligera rotación).
          Aunque esta ligera rotación del pie es una constante en muchos
          atletas, no es un gesto que se deba recomendar sino más bien un
          resultado de la biomecánica del atleta. La fase de impulso de la
          carrera puede considerarse la fase propulsiva o activa, mientras que
          la fase de amortiguamiento es negativa debido a que produce
          desaceleración, es por esto que algunos entrenadores enseñan a sus
          atletas a caer sobre el metatarso de una forma ligera como si
          estuvieran corriendo sobre una superficie con carbón ardiendo. El peso
          del atleta se convierte en una característica condicionante debido a
          que, a mayor peso, mayor será el gasto energético en el momento de la
          fase de amortiguación ocasionando mayor fricción con el suelo. Será
          una tarea para los entrenadores buscar estrategias que puedan
          contribuir con la disminución del tiempo de esta fase. (p. 20) <br />
          <br />
          Según
          <a class="cite">Rius (cómo se citó en Tobar y Zurata, 2013)</a> El
          contacto con el suelo siempre se debe hacer por delante del centro de
          gravedad, la mayor distancia del centro de gravedad dependerá de la
          longitud de la zancada, la fase de amortiguamiento inicia cuando el
          pie toma contacto con el suelo, el cual debe ser lo más suave posible
          evitando disminuir la fricción y por lo tanto mayor consumo energético
          para después hacer contacto con la parte externa del metatarso. Es
          considerado un error técnico que el pie entre en contacto con el
          talón. (p.20)
          <br />
          <br />
          <strong>Sostén</strong>
          <br />
          Esta fase inicia cuando el pie cae sobre el suelo apoyando el
          metatarso desde su parte externa llevando este sostén hasta la punta
          del pie cuando debe iniciar la fase de impulso. El sostén debe durar
          el menor tiempo posible debido a que en esta fase no hay aceleración
          por lo contrario se presenta desaceleración, pero es fundamental
          porque es el momento en el cual se realiza el apoyo del pie para
          continuar con la impulsión. La fase de sostén debe iniciar con la
          cadera baja posición ocasionada por la extensión de la pierna
          transmitiéndose hasta la extensión del tobillo.
          <br />
          <br />
          <strong>Acción de tronco y brazos</strong>
          <br />
          Estos pueden beneficiar o dificultar el desarrollo normal de la
          carrera, de la correcta colocación de tronco (en la vertical o
          ligeramente inclinado hacia delante) y de la acción coordinada de
          brazos (cuyo movimiento será convergente por delante y divergente por
          detrás, con una angulación de 90 grados en todo momento y un recorrido
          descrito por el puño desde la barbilla hasta el encuentro del mismo
          con la cadera) dependerá la eficacia de la acción de brazos en
          carrera. La posición de la mano es semicerrada (relajada) y, junto con
          los brazos, su función es la de equilibrar y compensar la acción de
          piernas y tronco en carrera. <br /><br />
          <a class="cite">
            Campos y Gallach (cómo se citó en Tobar y Zurata, 2013)
          </a>
          plantean que los brazos deben mantenerse flexionados por los codos con
          un ángulo de 90 grados, que varía según la distancia, más cerrados en
          velocidad que carreras largas. Las manos van semicerradas y se
          aconseja la colocación del pulgar sobre el índice. Otros aspectos que
          interfieren en la técnica de la carrera, mencionados por los autores
          Campos y Gallach, son la posición del tronco, el cual debe estar
          ligeramente inclinado, ya que ello facilita la acción de avance.
          Cuando más elevada es la velocidad, más inclinación hay en la carrera
          y viceversa. También mencionan la posición de la cabeza, que debe ir
          de un modo natural con la barbilla ligeramente baja, como un consejo
          se mira el suelo unos 15 metros adelante. Cabeza, tronco y caderas se
          mantienen en una misma línea y deben formar un solo bloque unido para
          mejorar el aprovechamiento del trabajo activo de las piernas. Es
          conveniente mantener máxima relajación en la musculatura que no
          interviene en la acción de la carrera, para ello la mirada ira al
          frente, la cara relajada sin apretar los dientes ni tensar los puños.
          (p. 22).
        </p>
