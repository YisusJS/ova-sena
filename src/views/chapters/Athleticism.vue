        <h2 class="module__title">Marcha Atletica</h2>
      </div>
      <div class="module__content">
        <p>
          A pesar de considerarse una actividad natural no lo es, marchar no es
          igual a caminar rápido, es necesario incorporar algunos movimientos
          técnicos propios de esta especialidad que no se realizan naturalmente.
          <br /><br />
          A finales del siglo XVIII tanto en Europa como en los Estados Unidos
          se practicaba sobre distancias de más de 1000 km., cómo la prueba de
          Torín- Marsella-Barcelona (1.100 km) que se disputó en 1901. Cómo
          especialidad incorporada en las pruebas atléticas aparece en 1876 en
          el campeonato de Inglaterra de 7 millas, aunque inicialmente se
          prefería competir sobre distancias superiores a 100 km. Fue
          incorporada en los Juegos olímpicos de Londres en 1908 con la
          distancia de 10 millas. Sin embargo, ha sido una especialidad que se
          ha sometido a gran debate y cambios debido al ajuste de las Técnicas y
          las distancias a competir. En 1956 se reglamentan los 20 y los 50 km.,
          para hombres; a nivel femenino sólo se incorpora hasta los Juegos
          Olímpicos de Barcelona 1992 con la distancia de los 10 km.,
          actualmente ya compiten en los 20 km. (Rius, 2005, p.22).
          <br /><br />
          <b>Técnica de la marcha atlética</b>
          <br />
          <a class="cite">Barreto, Villarroya y Calero (2017)</a>. Se podría
          afirmar que la marcha atlética tiene su origen en la marcha normal,
          pero se buscaron algunos ajustes a este movimiento natural con el fin
          de hacerla más efectiva y veloz, lo cual representó arreglos en la
          coordinación, en la amplitud de la zancada y la inclusión de algunas
          normas que al parecer buscaban que no dejara de ser marcha normal
          incrementando la velocidad y evitando que se convirtiera en carrera
          caracterizada por la fase de vuelo. Por lo tanto, encontrar la
          relación adecuada entre amplitud de la zancada y frecuencia de la
          misma sin proporcionar un gasto energético desproporcionado deberá ser
          uno de los objetivos técnicos de cada marchista, un buen resultado
          depende de una técnica adecuada. <br /><br />
          En la marcha atlética el reglamento exige que se debe mantener
          contacto permanente con el suelo sin flexionar la rodilla de la pierna
          que se lleva al frente en el momento que esta pasa por la vertical y
          realiza el apoyo con el talón adelante del cuerpo, obligando a
          extender la pierna y buscar amplitud. Las restricciones impuestas por
          el reglamento hacen que este movimiento deje de ser natural y por lo
          tanto requiere de muy buena adaptación coordinativa y muscular
          principalmente. <br /><br />
          El reglamento de competición de la Federación Internacional de
          Atletismo Amateur (IAAF), en su Art. 230, define la marcha atlética, y
          dice: "es una progresión de pasos ejecutados de modo que el atleta se
          mantenga en contacto con el suelo, a fin de que no se produzca pérdida
          de contacto visible (a simple vista). La pierna que avanza debe estar
          recta, (es decir, no doblada por la rodilla) desde el momento del
          primer contacto con el suelo hasta que se halle en posición vertical",
          este aspecto reglamentado implica movimientos específicos del
          deportista, los cuales son analizados biomecánica mente para potenciar
          la técnica y por ende el rendimiento deportivo. <br /><br />
          Respecto a la fase de vuelo, varios autores afirman que hay una
          pérdida de contacto, que está entre 0,005-0,05 ms, en dependencia de
          la velocidad y del tipo de marchista, sin embargo, es imperceptible al
          ojo humano. <br /><br />La duración del ciclo de la marcha atlética es
          significativamente menor al de la marcha normal, debido a la
          disminución del tiempo de apoyo, tras el apoyo del talón se produce un
          desplazamiento rápido de la carga al ante pie. <br /><br />
          El importante rango de movimiento del hombro (41, 9º de flexión y
          72,2º de extensión) colabora en el ascenso del CGC en el doble apoyo.
          Por otra parte, el importante rango de rotación de las cinturas
          escapular y pélvica ayuda a disminuir el gasto energético, almacenando
          energía elástica en los movimientos oblicuos del tronco. Además, la
          rotación pélvica contribuye al incremento de la longitud de la zancada
          y de la velocidad, evidenciándose una relación inversa de este
          parámetro con la velocidad, probablemente porque si la rotación es
          excesiva desciende más el CGC (doble apoyo), cuando hay que elevarlo,
          lo cual disminuye la velocidad de la marcha. (p.p. 54-55).
          <br /><br />
          Una de las características volitivas del marchista es la capacidad de
          soportar esfuerzos prolongados teniendo en cuenta que las pruebas
          reglamentadas son 20 y 50 km., además de muy buen ritmo, excelente
          movilidad de caderas que contribuye con la ejecución de un gesto
          técnico fluido, el cual debe disminuir el gasto energético , desde el
          punto de vista de capacidades físicas niveles de fuerza en el tren
          superior y optimo nivel de máximo consumo de oxígeno por ser un tipo
          de deporte con gran demanda e resistencia aeróbica. <br /><br />
          Descripción técnica: Debido al movimiento de la cadera los codos
          tienden a abrirse separándose del cuerpo cuando los brazos se
          desplazan hacia atrás. El tronco se gira compensando el avance de las
          caderas, al llevar la pierna que busca el apoyo adelante la cadera
          desciende producto de este movimiento, el contacto se realiza con el
          talón llevando la pierna extendida
